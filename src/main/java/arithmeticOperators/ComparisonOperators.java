package arithmeticOperators;

public class ComparisonOperators {
    public static void main(String[] args) {

        /*
        Comparison operators are used to compare 2 values:
        > more than
        <less than
        >=more or equal
        <=less or equal
        ==equal
        !=not equal
         */

        byte potato =5;
        byte fries =10;

        System.out.println(potato>fries);
        System.out.println(potato<fries);
        System.out.println(potato>=fries);
        System.out.println(potato<=fries);
        System.out.println(potato==fries);
        System.out.println(potato!=fries);
        //========================================================================


        byte flower = 25;
        byte aplle = 100;
        short world = 555;
        short orange = 120;
        int int1 = 340000;
        int int2 = 25600000;
        long computer = 6;
        long option = 35;

        System.out.println(flower>world);
        System.out.println(orange!=computer);
        System.out.println(aplle<option);
        System.out.println(int2==computer);
        System.out.println(computer>int1);
        System.out.println(orange!=int1);
        System.out.println(world==option);


        /*
        CONCTENATION
         */

        System.out.println(1+2);
        System.out.println(1+2+" hello");
        System.out.println("hello "+1+2);
        System.out.println("hello " +3+true);













    }
}
